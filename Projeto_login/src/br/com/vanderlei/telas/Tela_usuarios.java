    private void adicionar_dados(){
        String sql = "insert into tbusuario(iduser,usuario,fone,login,senha,perfil) values(?,?,?,?,?,?)";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1,edit_id_usuario_.getText());
            pst.setString(2,edit_id_nome_.getText());
            pst.setString(3,edit_id_fone_.getText());
            pst.setString(4,edit_id_login_.getText());
            pst.setString(5,edit_id_senha_.getText());
            pst.setString(6,jComboBox1_id_usuario_.getSelectedItem().toString());
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(null,"Usuario inserido com sucesso!");
                edit_id_nome_.setText(null);
                edit_id_fone_.setText(null);
                edit_id_login_.setText(null);
                edit_id_senha_.setText(null);
                jComboBox1_id_usuario_.setSelectedItem(null);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e);
            
        }
    }
    
    
    private void consultar(){
        String sql = "select * from tbusuario where  iduser=?";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1,edit_id_usuario_.getText());
            sr = pst.executeQuery();
            if (sr.next()) {
                edit_id_nome_.setText(sr.getString(2));
                edit_id_fone_.setText(sr.getString(3));
                edit_id_login_.setText(sr.getString(4));
                edit_id_senha_.setText(sr.getString(5));
                jComboBox1_id_usuario_.setSelectedItem(sr.getString(6));
            } else {
                JOptionPane.showMessageDialog(null,"NÃ£o existe usuario com este id");
                edit_id_nome_.setText(null);
                edit_id_fone_.setText(null);
                edit_id_login_.setText(null);
                edit_id_senha_.setText(null);
                jComboBox1_id_usuario_.setSelectedItem(null);
            }
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,e);
            System.out.println(e);
        }

    }

